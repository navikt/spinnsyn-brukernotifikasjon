package no.nav.helse.flex.cronjob

import no.nav.helse.flex.logger
import no.nav.helse.flex.service.VedtakStatusService
import org.springframework.scheduling.annotation.Scheduled
import org.springframework.stereotype.Component
import java.util.concurrent.TimeUnit

@Component
class ResendBrukernotifikasjonJob(
    val leaderElection: LeaderElection,
    val vedtakStatusService: VedtakStatusService,
) {
    private val log = logger()

    @Scheduled(
        initialDelay = 2,
        fixedDelay = 15,
        timeUnit = TimeUnit.MINUTES,
    )
    fun run() {
        if (leaderElection.isLeader()) {
            vedtakStatusService.rekjoer(rekjoerIder)
        } else {
            log.info("Kjører ikke cronjob siden denne podden ikke er leder.")
        }
    }
}

